# ****************************************************************************
# Переменные и требования к ним
# ****************************************************************************
# В Python всё объект. Числа, строки, массивы и даже функции и классы являются
# объектом. Переменная в Python является указателем на объект.
#
# Переменная "a" хранит значение "5". Верно. Также верно, что Python создал
# объект целого типа 5 (о типах данных мы поговорим на следующих лекциях).
# Далее была создана переменная "a" которая является указателем на объект целого
# типа число пять.
#
# Python является языком со строгой динамической типизацией. Это означает что
# тип объекта изменить невозможно он строго задаётся при создании объекта. При
# этом переменные могут ссылаться на объекты разных типов. И это не вызывает
# ошибки.
#
a = 5
print(a)
a = "hello world"
print(a)
a = 42.0 * 3.141592 / 2.71828
print(a)
#
# PEP-8! Обратите внимание на отступы в один пробел вокруг математических знаков
# "=", "*" и др. Это требование по оформлению кода, которое повышает его
# читаемость.
# Важно! Использование одной и той же переменной для разных типов данных
# является плохим стилем.
#
# Предварительная инициализация переменных в языке не нужна. Вы можете
# использовать переменную в том месте где оно понадобилось. Даже если уже
# написали несколько сотен строк кода.
#
#
# ****************************************************************************
# Константы
# ****************************************************************************
# Дополнительных команд для создания констант в языке Python нет. Есть лишь
# договорённость, что константа - это переменная написанное прописными буквами.
# Примеры констант.
#
MAX_COUNT = 1000
ZERO = 0
DATA_AFTER_DELETE = 'No data'
